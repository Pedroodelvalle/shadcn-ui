function DraggableRow({ row }: { row: Row<z.infer<typeof schema>> }) {
  const { transform, transition, setNodeRef, isDragging } = useSortable({
    id: row.original.id,
  })

  return (
    <TableRow
      data-state={row.getIsSelected() && "selected"}
      data-dragging={isDragging}
      ref={setNodeRef}
      className="relative z-0 data-[dragging=true]:z-10 data-[dragging=true]:opacity-80"
      style={{
        transform: CSS.Transform.toString(transform),
        transition: transition,
      }}
    >
      {row.getVisibleCells().map((cell) => (
        <TableCell key={cell.id}>
          {flexRender(cell.column.columnDef.cell, cell.getContext())}
        </TableCell>
      ))}
    </TableRow>
  )
}

export function DataTable({
  data: initialData,
  videoData,
}: {
  data: z.infer<typeof schema>[]
  videoData?: z.infer<typeof videoSchema>[]
}) {
  const t = useTranslations("dashboard")
  const [data, setData] = React.useState(() => initialData)
  const [rowSelection, setRowSelection] = React.useState({})
  const [columnVisibility, setColumnVisibility] =
    React.useState<VisibilityState>({})
  const [columnFilters, setColumnFilters] = React.useState<ColumnFiltersState>(
    []
  )
  const [sorting, setSorting] = React.useState<SortingState>([])
  const [pagination, setPagination] = React.useState({
    pageIndex: 0,
    pageSize: 10,
  })
  const [activeTab, setActiveTab] = React.useState("posted")
  const sortableId = React.useId()
  const sensors = useSensors(
    useSensor(MouseSensor, {}),
    useSensor(TouchSensor, {}),
    useSensor(KeyboardSensor, {})
  )

  // Filter video data based on active tab
  const filteredVideoData = React.useMemo(() => {
    if (!videoData) return []
    switch (activeTab) {
      case "posted":
        return videoData.filter(video => video.status === "posted")
      case "pending-approval":
        return videoData.filter(video => video.status === "pending")
      case "top-videos":
        return videoData
          .filter(video => video.status === "posted")
          .sort((a, b) => {
            const viewsA = parseFloat(a.views.replace(/[^\d.]/g, '')) * (a.views.includes('M') ? 1000000 : a.views.includes('K') ? 1000 : 1)
            const viewsB = parseFloat(b.views.replace(/[^\d.]/g, '')) * (b.views.includes('M') ? 1000000 : b.views.includes('K') ? 1000 : 1)
            return viewsB - viewsA
          })
      default:
        return videoData
    }
  }, [videoData, activeTab])

  const dataIds = React.useMemo<UniqueIdentifier[]>(
    () => data?.map(({ id }) => id) || [],
    [data]
  )

  const table = useReactTable({
    data,
    columns,
    state: {
      sorting,
      columnVisibility,
      rowSelection,
      columnFilters,
      pagination,
    },
    getRowId: (row) => row.id.toString(),
    enableRowSelection: true,
    onRowSelectionChange: setRowSelection,
    onSortingChange: setSorting,
    onColumnFiltersChange: setColumnFilters,
    onColumnVisibilityChange: setColumnVisibility,
    onPaginationChange: setPagination,
    getCoreRowModel: getCoreRowModel(),
    getFilteredRowModel: getFilteredRowModel(),
    getPaginationRowModel: getPaginationRowModel(),
    getSortedRowModel: getSortedRowModel(),
    getFacetedRowModel: getFacetedRowModel(),
    getFacetedUniqueValues: getFacetedUniqueValues(),
  })

  function handleDragEnd(event: DragEndEvent) {
    const { active, over } = event
    if (active && over && active.id !== over.id) {
      setData((data) => {
        const oldIndex = dataIds.indexOf(active.id)
        const newIndex = dataIds.indexOf(over.id)
        return arrayMove(data, oldIndex, newIndex)
      })
    }
  }

  return (
    <Tabs
      defaultValue="posted"
      value={activeTab}
      onValueChange={setActiveTab}
      className="w-full flex-col justify-start gap-6"
    >
      <div className="flex items-center justify-end px-4 lg:px-6">
        <Label htmlFor="view-selector" className="sr-only">
          View
        </Label>
        <Select defaultValue="posted">
          <SelectTrigger
            className="flex w-fit @4xl/main:hidden"
            size="sm"
            id="view-selector"
          >
            <SelectValue placeholder={t("selectView")} />
          </SelectTrigger>
          <SelectContent>
            <SelectItem value="posted">{t("posted")}</SelectItem>
            <SelectItem value="pending-approval">{t("pendingApproval")}</SelectItem>
            <SelectItem value="top-videos">{t("topVideos")}</SelectItem>
          </SelectContent>
        </Select>
        <TabsList className="**:data-[slot=badge]:bg-muted-foreground/30 hidden **:data-[slot=badge]:size-5 **:data-[slot=badge]:rounded-full **:data-[slot=badge]:px-1 @4xl/main:flex">
          <TabsTrigger value="posted">{t("posted")}</TabsTrigger>
          <TabsTrigger value="pending-approval">
            {t("pendingApproval")} <Badge variant="secondary">3</Badge>
          </TabsTrigger>
          <TabsTrigger value="top-videos">
            {t("topVideos")} <Badge variant="secondary">6</Badge>
          </TabsTrigger>
        </TabsList>
      </div>
      <TabsContent
        value="posted"
        className="relative flex flex-col gap-4 overflow-auto px-4 lg:px-6"
      >
        <VideoTable data={filteredVideoData} t={t} />
      </TabsContent>
      <TabsContent
        value="pending-approval"
        className="relative flex flex-col gap-4 overflow-auto px-4 lg:px-6"
      >
        <VideoTable data={filteredVideoData} t={t} />
      </TabsContent>
      <TabsContent
        value="top-videos"
        className="relative flex flex-col gap-4 overflow-auto px-4 lg:px-6"
      >
        <VideoTable data={filteredVideoData} t={t} />
      </TabsContent>
    </Tabs>
  )
}

